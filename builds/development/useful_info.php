<div class="container pt90 pb90">
	<div class="title-heading1 ">
		<h3>How to write my research proposal?</h3>
	</div>
	<div class="row justify-content-center">
		<div class="col-lg-10">
			<p>
				Your research proposal is one of the important documents you should prepare as part of the application process to secure a doctoral offer at the university. A research proposal is a concise and coherent summary of your proposed research. You should spend some time writing your research proposal as it is essential to help us to match your research interest with an appropriate supervisor.
			</p>
			<h4>A typical research proposal:</h4>
			<ul>
				<li>Ranges from 1,000 to 3,000 words</li>
				<li>Is judged both on content and proposal format </li>
			</ul>
			<h4>The research proposal should:</h4>
			<ul>
				<li>Clearly define the topic you are interested in and show you understand your research area.</li>
				<li>Include research questions.</li>
				<li>Demonstrate you understand how to conduct research.</li>
				<li>Look professional – it should be typed, in good English, well-structured with suitable headings.</li>
			</ul>
			<h4>Your research proposal should contain:</h4>
			<div class="row">
				<div class="col-lg-6">
					<ol>
						<li>
							<h5>Title</h5>
						</li>
						<li>
							<h5>Abstract</h5>
							<p>In the abstract you need to clarify and state the problem that you want to examine or the central question that wish to address.</p>
						</li>
						<li>
							<h5>Research Overview (Introduction)</h5>
							<p>A well-written introduction is the most efficient way to hook your reader and set the context of your proposed research. An introduction should include a review of relevant literature and theories relating to your proposed research area with summarising the current state of knowledge and recent debates on the topic.</p>
						</li>
						<li>
							<h5>Objectives and Questions</h5>
							<p>Your research proposal should state clearly your research objectives and what is (are) the main question(s) that you want to answer at the end of your research. Your question(s) needs to be answerable within a set timeframe.</p>
						</li>
					</ol>
				</div>
				<div class="col-lg-6">
					<ol start="5">
						<li>
							<h5>Methodology</h5>
							<p>Present all research methods you intend to use throughout your research, e.g., techniques, sample size, equipment, data analysis, species choices, and explain why it is the most appropriate methodology to effectively answer the research questions.</p>
						</li>
						<li>
							<h5>Significance of Research</h5>
							<p>Discuss if your research will make an original contribution within your subject area.</p>
						</li>
						<li>
							<h5>Bibliography</h5>
							<p>Acknowledge ALL books, articles, journal papers, websites and all relevant references that you used to write your proposal.</p>
						</li>
					</ol>
					<p><i>You can still make changes to your research proposal once you have been accepted for a research degree, however as it is the foundation of your working relationship with your supervisor, it cannot be radically altered without discussion and consultation.</i></p>
				</div>
			</div>
		</div>
	</div>
</div>

